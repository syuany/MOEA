function f = ZDT6(x)
    % ZDT6函数实现，输入x为决策变量向量（维度m≥2），输出f为两个目标函数值
    m = length(x);  % 决策变量维度
    % 计算f1
    f1 = 1 - exp(-4 * x(1)) * (sin(6 * pi * x(1)))^6;
    % 计算g(x)
    sum_x = sum(x(2:m));  % 对x(2)到x(m)求和
    g = 1 + (m - 1) * ((sum_x) / (m - 1))^0.25;  % 等价于1 + (sum_x)^0.25
    % 计算f2
    f2 = g * (1 - (f1 / g)^2);
    % 输出目标函数向量
    f = [f1; f2];
end